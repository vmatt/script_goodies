def calculate_session_length(group):
    connected = group[group['status'] == 'Connected']
    disconnected = group[group['status'] == 'Disconnected']
    group['session_length'] = None

    for index, row in disconnected.iterrows():
        match = connected[(connected['ip'] == row['ip']) & (connected['datetime'] < row['datetime'])]
        if not match.empty:
            session_start = match.iloc[-1]['datetime']
            group.loc[index, 'session_length'] = row['datetime'] - session_start

    return group

def format_session_length(td):
    hours, remainder = divmod(td.seconds, 3600)
    minutes, seconds = divmod(remainder, 60)
    return f"{td.days * 24 + hours:02}:{minutes:02}:{seconds:02}"


df = df.groupby(['user']).apply(calculate_session_length)
df['session_length'] = df['session_length'].apply(lambda x: format_session_length(x) if pd.notnull(x) else x)
df.to_csv('openvpn_session_length.csv', index=False)

